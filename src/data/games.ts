export function getLevelInfo(xp: number) {
  return LEVELS.find((l) => xp >= l.minXP && xp <= l.maxXP) || LEVELS[0];
}

export function getNextLevel(xp: number) {
  const current = getLevelInfo(xp);
  return LEVELS[current.level] || null;
}
